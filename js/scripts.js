function firstPar() {
    let e = document.getElementById('first-par');

    if (e.style.display === 'none') {
        e.style.display = 'block';
    } else {
        e.style.display = 'none';
    }
}

function secPar() {
    let e = document.getElementById('second-par');

    if (e.style.display === 'none') {
        e.style.display = 'block';
    } else {
        e.style.display = 'none';
    }
}